namespace _13_for_loop
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // for loop -> count controlled loop, FINITE amount of times

            for (int i = 0; i <= 10; i++) //) //in brackets: index, condition to stop, smth you want to run every loop
            {
                Console.WriteLine(i);
            }

            Console.ReadKey();
        }
    }
